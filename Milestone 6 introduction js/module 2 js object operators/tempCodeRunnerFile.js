"use strict"
// introduction to the js unary operators..............................................
// Decrement operators
let num = 10;
num--;
console.log(num);
console.log(typeof num);
// increment  operators
let age = 20;
++age;
console.log(age);
// unary minus
let a = "40";
let b = -a;
console.log(b);
console.log(typeof b);
//  unary plus
let y = "10";
let x = +y;
console.log(x);
console.log(typeof x);